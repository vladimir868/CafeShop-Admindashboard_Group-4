    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        
        
        String Username = username.getText().trim();
        String Password = password.getText().trim();
        
        // Check if the checkboxes are selected
        boolean isAdminSelected = Admin.isSelected();
        boolean isCashierSelected = Cashier.isSelected();
        
        if (Username.isEmpty() || Password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in both fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // If both checkboxes are selected or neither is selected, show an error
        if (isAdminSelected && isCashierSelected) {
            JOptionPane.showMessageDialog(this, "Please select only one user type.", "Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
         // If the user selects Admin
        if (isAdminSelected) {
            if (Username.equals(ADMIN_USERNAME) && Password.equals(ADMIN_PASSWORD)) {
                // Open Admin Dashboard
                Admin_dashboard adminDash = new Admin_dashboard();
                setVisible(false);
                adminDash.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid admin username or password.", "Login Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
       
        // If the user selects Cashier
        else if (isCashierSelected) {
            String filePath = "AccountSystem.txt";
            boolean accountExists = false;

            // Read the accounts file to find a match for the cashier login
            try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
                String line;
                boolean isCorrectPassword = false;

                while ((line = reader.readLine()) != null) {
                    if (line.startsWith("Username: " + Username)) {
                        // Found username, now check password
                        isCorrectPassword = checkPasswordForAccount(reader, Password);
                        accountExists = true;
                        break;
                    }
                }

                if (accountExists && isCorrectPassword) {
                    // Open Cashier Dashboard if credentials match
                    Cashier_dashboard cashierDash = new Cashier_dashboard();
                    setVisible(false);
                    cashierDash.setVisible(true);
                } else {
                    // Show error message if account or password doesn't match
                    JOptionPane.showMessageDialog(this, "Invalid username or password.", "Login Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error reading accounts file.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // If no checkbox is selected
            JOptionPane.showMessageDialog(this, "Please select user type (Admin or Cashier).", "Selection Error", JOptionPane.ERROR_MESSAGE);
        }
    

    }//GEN-LAST:event_LoginActionPerformed

