    // Helper method to check the password for the username from the file
    private boolean checkPasswordForAccount(BufferedReader reader, String enteredPassword) throws IOException {
        String line;
        boolean foundPassword = false;

        while ((line = reader.readLine()) != null) {
            if (line.startsWith("Password: " + enteredPassword)) {
                foundPassword = true;
                break;
            }
        }
        return foundPassword;
    }

    
